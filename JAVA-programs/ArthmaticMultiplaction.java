public class ArthmaticMultiplaction{
	//Multiplaction Operator 
	public int MultiplactionInteger(int number1, int number2){
	return number1* number2;
	}
	
	public float MultiplactionFloat(float number1, float number2){
		return number1* number2;
	}
	
	public double MultiplactionDouble(double number1, double number2){
		return number1* number2;
	}
	
	public long MultiplactionLong (long number1, long number2){
		return number1* number2;
	}
	
	
}


